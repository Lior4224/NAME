public class Animal{
  public String _name;
  public int _health;
  public int _collectTime;
  public int _survivability;
  public int _hunger;

  public Animal (String name, int health, int collectTime, int survivability, int hunger) {
    _name = name;
    _health = health;
    _collectTime = collectTime;
    _survivability = survivability;
    _hunger = hunger;
  }
}
